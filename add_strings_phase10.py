def add_strings(lang: str, entries: list) -> int:
    folder = os.path.join(BASE, LOCALE_MAP[lang])
    path = os.path.join(folder, "strings.xml")
    with open(path, encoding="utf-8") as f:
        content = f.read()
    count = 0
    for key, val in entries:
        if f'name="{key}"' in content:
            continue
        val_escaped = val.replace("'", "\\'").replace("&", "&amp;")
        entry = f'    <string name="{key}">{val_escaped}</string>'
        content = content.replace("</resources>", f"{entry}\n</resources>")
        count += 1
    with open(path, "w", encoding="utf-8") as f:
        f.write(content)
    return count


